exports.generateOrcName = function () {
  if (orcs.length === 0) {
    var shasum = crypto.createHash ('sha1');
    shasum.update (Math.floor (Math.random ()));
    shasum.update ('ORC-CLIENT');
    return shasum.digest ('hex');
  }
  return orcs.splice (parseInt (Math.floor (Math.random () * orcs.length)), 1)[0];
};
